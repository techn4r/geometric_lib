def area(a, b, c):
    ''' Функция возвращает площадь треугольника по формуле S = (a + b + с) / 2, где a,b,c -  стороны треугольника.'''
    return (a + b + c) / 2


def perimeter(a, b, c):
    ''' Функция возвращает периметр треугольника по формуле P = a + b + с, где a,b,c -  стороны треугольника.'''
    return a + b + c
